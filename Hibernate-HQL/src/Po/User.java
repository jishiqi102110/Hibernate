package Po;

public class User {
	private int id;
	private String name;
	private IDCard idCard;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public IDCard getidCard() {
		return idCard;
	}
	public void setidCart(IDCard idCart) {
		this.idCard = idCart;
	}
	

}
